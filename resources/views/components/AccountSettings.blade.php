    // Tab Switching + Persistence
    document.addEventListener('DOMContentLoaded', () => {
        const links = document.querySelectorAll('.nav-link');
        const sections = document.querySelectorAll('.content-section');
        const activeTab = localStorage.getItem('activeTab') || 'profile-settings';

        // Set active tab on load
        sections.forEach(sec => {
            sec.classList.toggle('active', sec.id === activeTab);
            sec.classList.toggle('hidden', sec.id !== activeTab);
        });

        links.forEach(link => {
            link.classList.toggle('active', link.dataset.target === activeTab);
            link.addEventListener('click', (e) => {
                e.preventDefault();
                const target = link.dataset.target;
                localStorage.setItem('activeTab', target);

                // Update active state
                links.forEach(l => l.classList.remove('active'));
                link.classList.add('active');
                sections.forEach(s => {
                    s.classList.toggle('active', s.id === target);
                    s.classList.toggle('hidden', s.id !== target);
                });
            });
        });
    });

    function togglePassword(fieldId) {
