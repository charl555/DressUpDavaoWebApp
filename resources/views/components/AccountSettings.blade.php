    function measurementsForm() {
        return {
            showForm: false,
            form: {
                chest: Number('{{ Auth::user()->user_measurements->chest ?? 0 }}') || '',
                waist: Number('{{ Auth::user()->user_measurements->waist ?? 0 }}') || '',
                hips: Number('{{ Auth::user()->user_measurements->hips ?? 0 }}') || '',
                shoulder: Number('{{ Auth::user()->user_measurements->shoulder ?? 0 }}') || ''
            },
            errors: {},
            generalError: '',
            successMessage: '',
            loading: false,

            validateField(field) {
                let value = this.form[field];
                this.errors[field] = '';

                if (value === '' || value === null || (typeof value === 'number' && isNaN(value))) {
                    this.errors[field] = `${field.charAt(0).toUpperCase() + field.slice(1)} measurement is required.`;
                    return;
                }

                let min, max, errorMsg;
                switch (field) {
                    case 'chest': min = 20; max = 60; errorMsg = 'Chest must be between 2060 inches.'; break;
                    case 'waist': min = 20; max = 50; errorMsg = 'Waist must be between 2050 inches.'; break;
                    case 'hips': min = 20; max = 60; errorMsg = 'Hips must be between 2060 inches.'; break;
                    case 'shoulder': min = 10; max = 30; errorMsg = 'Shoulder must be between 1030 inches.'; break;
                    default: return;
                }

                if (value < min || value > max) {
                    this.errors[field] = errorMsg;
                }
            },

            validateForm() {
                this.validateField('chest');
                this.validateField('waist');
                this.validateField('hips');
                this.validateField('shoulder');
                return Object.values(this.errors).every(err => !err);
            },

            async submitMeasurements() {
                if (!this.validateForm()) return;

                this.loading = true;
                this.generalError = '';
                this.successMessage = '';

                try {
                    const response = await fetch("{{ route('measurements.update') }}", {
                        method: "POST",
                        headers: {
                            "Content-Type": "application/json",
                            "X-CSRF-TOKEN": document.querySelector('meta[name="csrf-token"]').content
                        },
                        body: JSON.stringify(this.form)
                    });

                    const data = await response.json();

                    if (response.ok) {
                        this.successMessage = data.message || 'Measurements saved successfully!';
                        this.showForm = false;
                        setTimeout(() => window.location.reload(), 1500);
                    } else {
                        this.generalError = data.message || 'Failed to save measurements.';
                    }
                } catch (err) {
                    this.generalError = 'An error occurred. Please try again.';
                } finally {
                    this.loading = false;
                }
            }
        }
    }

    function openReviewModal(shopId, shopName, isUpdate = false) {
        document.getElementById('modalShopId').value = shopId;
        document.getElementById('modalShopName').innerText = shopName;
        document.getElementById('modalActionLabel').innerText = isUpdate ? 'Update' : 'Leave';

        fetch(`/user/review-data/${shopId}`)
            .then(res => res.json())
            .then(data => {
                document.getElementById('modalBookingCount').innerText =
                    `(${data.bookingCount} ${data.bookingCount > 1 ? 'purchases' : 'purchase'})`;

                document.getElementById('modalRating').value = data.review?.rating || '';
                document.getElementById('modalComment').value = data.review?.comment || '';
            });

        document.getElementById('reviewModal').classList.remove('hidden');
        document.getElementById('reviewModal').classList.add('flex');
    }

    function closeReviewModal() {
        document.getElementById('reviewModal').classList.remove('flex');
        document.getElementById('reviewModal').classList.add('hidden');
    }
